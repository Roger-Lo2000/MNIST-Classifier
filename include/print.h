// This file can print 1d vector, 2d vector with int variable and float variable.
#include <vector>
void print(std::vector<float> x); // 1d float vector
void print(std::vector<int> x); // 1d int vector
void print(std::vector<std::vector<float>> x); // 2d float vector
void print(std::vector<std::vector<int>> x); // 2d int vector
